#!/usr/bin/env python3
"""Post-process the openapi-generator Rust output to produce the Camunda Domain Type System.

openapi-generator does not handle Camunda's "semantic key" schemas correctly. A semantic
key is a `type: string` schema carrying `x-semantic-type` (e.g. JobKey, ProcessInstanceKey)
or an `allOf: [<otherKey>]` wrapper (e.g. JobKeyExactMatch). For these the generator either:

  * emits a broken empty struct  -> `pub struct ScopeKey {}`  (serializes as `{}`, not a string)
  * or drops the model entirely   -> generated code references `models::JobKey` that never exists

Both are wrong. This script replaces them with validated nominal newtypes over the underlying
string value, mirroring the C#/TS SDKs' branded "Camunda Domain Type System":

    #[serde(transparent)]
    pub struct JobKey(String);
    impl JobKey { assume_exists / try_new / is_valid / value ... }

It also fixes a known generator bug where inline schema refs produce a doubled module path
(`models::models::X`).

This script is idempotent and is invoked by scripts/generate.sh after generation.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path


def snake_case(name: str) -> str:
    s = re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", name)
    s = re.sub(r"([A-Z]+)([A-Z][a-z])", r"\1_\2", s)
    return s.lower()


def load_json(path: Path):
    with path.open() as f:
        return json.load(f)


def is_string_scalar(schema: dict | None) -> bool:
    return bool(schema) and schema.get("type") == "string" and "enum" not in schema


def ref_name(ref: str) -> str:
    return ref.split("/")[-1]


def resolve_constraints(name: str, schemas: dict, seen: set[str] | None = None) -> dict:
    """Walk the allOf chain to resolve pattern/minLength/maxLength for a semantic key."""
    if seen is None:
        seen = set()
    schema = schemas.get(name)
    if not schema or name in seen:
        return {}
    seen.add(name)
    out = {
        "pattern": schema.get("pattern"),
        "minLength": schema.get("minLength"),
        "maxLength": schema.get("maxLength"),
        "description": schema.get("description") or schema.get("title"),
        "example": schema.get("example"),
    }
    for sub in schema.get("allOf", []):
        ref = sub.get("$ref")
        if not ref:
            continue
        inherited = resolve_constraints(ref_name(ref), schemas, seen)
        for k in ("pattern", "minLength", "maxLength"):
            if out.get(k) is None:
                out[k] = inherited.get(k)
        if not out.get("description"):
            out["description"] = inherited.get("description")
    return out


def discover_domain_keys(schemas: dict) -> list[dict]:
    """Find all semantic-key schemas: string scalars with x-semantic-type, or
    single-ref allOf wrappers over another string scalar (e.g. *ExactMatch)."""
    keys: list[dict] = []
    for name, schema in schemas.items():
        if not is_string_scalar(schema):
            continue
        is_key = False
        if schema.get("x-semantic-type"):
            is_key = True
        else:
            refs = [ref_name(s["$ref"]) for s in schema.get("allOf", []) if s.get("$ref")]
            if len(refs) == 1 and is_string_scalar(schemas.get(refs[0])):
                is_key = True
        if is_key:
            c = resolve_constraints(name, schemas)
            c["name"] = name
            keys.append(c)
    keys.sort(key=lambda k: k["name"])
    return keys


def rust_string_literal(value: str) -> str:
    """Emit a Rust raw string literal, choosing a hash count that avoids the closing
    delimiter appearing in the content."""
    hashes = "#"
    while f'"{hashes}' in value:
        hashes += "#"
    return f'r{hashes}"{value}"{hashes}'


HEADER = """// @generated by scripts/postprocess_domain_types.py — DO NOT EDIT.
//
// The Camunda Domain Type System: nominal newtypes over the underlying string value
// for every semantic key in the OpenAPI spec (e.g. JobKey, ProcessInstanceKey).
//
// Each key serializes transparently as its string value. Use `try_new` to construct
// with constraint validation, or `assume_exists` to side-load a value received out of
// band (e.g. from configuration) without validation.

use serde::{Deserialize, Serialize};
use std::fmt;
use std::str::FromStr;
use std::sync::LazyLock;

/// Error returned when a value does not satisfy a Camunda Domain Type's constraints.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct CamundaKeyError {
    pub type_name: &'static str,
    pub message: String,
}

impl fmt::Display for CamundaKeyError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "invalid {}: {}", self.type_name, self.message)
    }
}

impl std::error::Error for CamundaKeyError {}

/// Shared behaviour for every Camunda Domain Type (semantic key newtype).
pub trait CamundaKey: Sized {
    /// The semantic type name as declared in the OpenAPI spec.
    const SEMANTIC_TYPE: &'static str;

    /// Construct without validation. Use when side-loading values not received from an API call.
    fn assume_exists(value: impl Into<String>) -> Self;

    /// Construct with constraint validation.
    fn try_new(value: impl Into<String>) -> Result<Self, CamundaKeyError>;

    /// Returns true if the value satisfies this type's constraints.
    fn is_valid(value: &str) -> bool;

    /// The underlying string value.
    fn value(&self) -> &str;
}

fn check_constraints(
    type_name: &'static str,
    value: &str,
    pattern: Option<&LazyLock<regex::Regex>>,
    min_length: Option<usize>,
    max_length: Option<usize>,
) -> Result<(), CamundaKeyError> {
    let len = value.chars().count();
    if let Some(min) = min_length {
        if len < min {
            return Err(CamundaKeyError {
                type_name,
                message: format!("length {len} is shorter than minimum {min}"),
            });
        }
    }
    if let Some(max) = max_length {
        if len > max {
            return Err(CamundaKeyError {
                type_name,
                message: format!("length {len} exceeds maximum {max}"),
            });
        }
    }
    if let Some(re) = pattern {
        if !re.is_match(value) {
            return Err(CamundaKeyError {
                type_name,
                message: format!("value {value:?} does not match required pattern"),
            });
        }
    }
    Ok(())
}
"""


def render_key(key: dict) -> str:
    name = key["name"]
    pattern = key.get("pattern")
    min_len = key.get("minLength")
    max_len = key.get("maxLength")
    desc = (key.get("description") or "").strip().replace("\r", " ").replace("\n", " ")
    example = key.get("example")

    lines: list[str] = []
    doc = desc if desc else f"Camunda domain type `{name}`."
    lines.append(f"/// {doc}")
    if example is not None:
        lines.append(f"///")
        lines.append(f"/// Example: `{example}`")

    # Lazily-compiled regex, one static per key that has a pattern.
    pattern_const = "None"
    if pattern:
        re_static = f"{snake_case(name).upper()}_PATTERN"
        lines.append(
            f"static {re_static}: LazyLock<regex::Regex> = "
            f"LazyLock::new(|| regex::Regex::new({rust_string_literal(pattern)}).expect(\"valid regex\"));"
        )
        pattern_const = f"Some(&{re_static})"

    min_expr = f"Some({min_len})" if min_len is not None else "None"
    max_expr = f"Some({max_len})" if max_len is not None else "None"

    lines.append(
        "#[derive(Clone, Default, Debug, PartialEq, Eq, Hash, PartialOrd, Ord, Serialize, Deserialize)]"
    )
    lines.append("#[serde(transparent)]")
    lines.append(f"pub struct {name}(String);")
    lines.append("")
    lines.append(f"impl {name} {{")
    lines.append("    /// Construct without validation. Use when side-loading values not received from an API call.")
    lines.append("    pub fn assume_exists(value: impl Into<String>) -> Self {")
    lines.append("        Self(value.into())")
    lines.append("    }")
    lines.append("")
    lines.append("    /// Construct with constraint validation.")
    lines.append("    pub fn try_new(value: impl Into<String>) -> Result<Self, CamundaKeyError> {")
    lines.append("        let v = value.into();")
    lines.append(
        f"        check_constraints(<Self as CamundaKey>::SEMANTIC_TYPE, &v, {pattern_const}, {min_expr}, {max_expr})?;"
    )
    lines.append("        Ok(Self(v))")
    lines.append("    }")
    lines.append("")
    lines.append("    /// Returns true if the value satisfies this type's constraints.")
    lines.append("    pub fn is_valid(value: &str) -> bool {")
    lines.append(
        f"        check_constraints(<Self as CamundaKey>::SEMANTIC_TYPE, value, {pattern_const}, {min_expr}, {max_expr}).is_ok()"
    )
    lines.append("    }")
    lines.append("")
    lines.append("    /// The underlying string value.")
    lines.append("    pub fn value(&self) -> &str {")
    lines.append("        &self.0")
    lines.append("    }")
    lines.append("")
    lines.append("    /// Consume the newtype and return the underlying string value.")
    lines.append("    pub fn into_value(self) -> String {")
    lines.append("        self.0")
    lines.append("    }")
    lines.append("}")
    lines.append("")
    lines.append(f"impl CamundaKey for {name} {{")
    lines.append(f"    const SEMANTIC_TYPE: &'static str = {json.dumps(name)};")
    lines.append("    fn assume_exists(value: impl Into<String>) -> Self { Self::assume_exists(value) }")
    lines.append("    fn try_new(value: impl Into<String>) -> Result<Self, CamundaKeyError> { Self::try_new(value) }")
    lines.append("    fn is_valid(value: &str) -> bool { Self::is_valid(value) }")
    lines.append("    fn value(&self) -> &str { self.value() }")
    lines.append("}")
    lines.append("")
    lines.append(f"impl fmt::Display for {name} {{")
    lines.append("    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {")
    lines.append("        f.write_str(&self.0)")
    lines.append("    }")
    lines.append("}")
    lines.append("")
    lines.append(f"impl From<{name}> for String {{")
    lines.append(f"    fn from(v: {name}) -> String {{ v.0 }}")
    lines.append("}")
    lines.append("")
    lines.append(f"impl AsRef<str> for {name} {{")
    lines.append("    fn as_ref(&self) -> &str { &self.0 }")
    lines.append("}")
    lines.append("")
    lines.append(f"impl FromStr for {name} {{")
    lines.append("    type Err = CamundaKeyError;")
    lines.append("    fn from_str(s: &str) -> Result<Self, Self::Err> { Self::try_new(s) }")
    lines.append("}")
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--client-dir", required=True)
    ap.add_argument("--spec", required=True)
    ap.add_argument("--metadata", required=False)
    args = ap.parse_args()

    client_dir = Path(args.client_dir)
    models_dir = client_dir / "src" / "models"
    spec = load_json(Path(args.spec))
    schemas = spec.get("components", {}).get("schemas", {})

    keys = discover_domain_keys(schemas)
    if not keys:
        print("[postprocess] ERROR: no domain keys discovered", file=sys.stderr)
        return 1

    # 1. Emit the Domain Type System module.
    body = HEADER + "\n" + "\n".join(render_key(k) for k in keys)
    (models_dir / "camunda_keys.rs").write_text(body)
    print(f"[postprocess] wrote {models_dir / 'camunda_keys.rs'} ({len(keys)} domain keys)")

    # 2. Remove the broken per-key model files openapi-generator emitted, and rewrite
    #    src/models/mod.rs so every domain key resolves to our newtype.
    key_modules = {snake_case(k["name"]) for k in keys}
    removed = 0
    for mod in key_modules:
        f = models_dir / f"{mod}.rs"
        if f.exists():
            f.unlink()
            removed += 1
    print(f"[postprocess] removed {removed} broken generated key model files")

    mod_rs = models_dir / "mod.rs"
    text = mod_rs.read_text()
    new_lines = []
    key_type_names = {k["name"] for k in keys}
    for line in text.splitlines():
        m = re.match(r"\s*pub mod (\w+);", line)
        if m and m.group(1) in key_modules:
            continue
        m = re.match(r"\s*pub use self::\w+::\{?\s*(\w+)", line)
        if m and m.group(1) in key_type_names:
            continue
        # Drop any prior camunda_keys wiring so re-runs stay idempotent.
        if re.match(r"\s*pub (mod|use(\s+self)?::?) ?camunda_keys", line) or "camunda_keys" in line:
            continue
        new_lines.append(line)
    new_lines.append("")
    new_lines.append("pub mod camunda_keys;")
    new_lines.append("pub use self::camunda_keys::*;")
    new_lines.append("")
    mod_rs.write_text("\n".join(new_lines))
    print("[postprocess] rewrote src/models/mod.rs to export the Domain Type System")

    # 3. Fix the doubled module-path bug (`models::models::X` -> `models::X`).
    fixed_files = 0
    for rs in client_dir.glob("src/**/*.rs"):
        content = rs.read_text()
        if "models::models::" in content:
            rs.write_text(content.replace("models::models::", "models::"))
            fixed_files += 1
    if fixed_files:
        print(f"[postprocess] fixed doubled module path in {fixed_files} file(s)")

    # 3b. Replace the generator's bare `Object` placeholder (emitted for unresolved
    #     free-form object schemas) with `serde_json::Value`.
    object_fixed = 0
    for rs in client_dir.glob("src/**/*.rs"):
        content = rs.read_text()
        new = re.sub(r"<Object>", "<serde_json::Value>", content)
        new = re.sub(r": Object\b", ": serde_json::Value", new)
        if new != content:
            rs.write_text(new)
            object_fixed += 1
    if object_fixed:
        print(f"[postprocess] replaced bare `Object` placeholder in {object_fixed} file(s)")

    # 4. Ensure the generated crate depends on `regex` (used by key pattern validation).
    cargo = client_dir / "Cargo.toml"
    cargo_text = cargo.read_text()
    if re.search(r"^regex\s*=", cargo_text, re.MULTILINE) is None:
        cargo_text = re.sub(
            r"(\[dependencies\]\n)",
            r'\1regex = "^1"\n',
            cargo_text,
            count=1,
        )
        cargo.write_text(cargo_text)
        print("[postprocess] added regex dependency to client/Cargo.toml")

    # 5. Silence clippy/rustc lints in the generated crate (it is never hand-edited,
    #    and openapi-generator output is not lint-clean).
    lib_rs = client_dir / "src" / "lib.rs"
    lib_text = lib_rs.read_text()
    if "#![allow(clippy::all)]" not in lib_text:
        lib_rs.write_text(
            "#![allow(clippy::all)]\n#![allow(warnings)]\n" + lib_text
        )
        print("[postprocess] silenced lints in generated client/src/lib.rs")

    # 6. Remove openapi-generator scaffolding noise that is irrelevant to this repo.
    for noise in ("git_push.sh", ".openapi-generator-ignore"):
        f = client_dir / noise
        if f.exists():
            f.unlink()

    print("[postprocess] done")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
