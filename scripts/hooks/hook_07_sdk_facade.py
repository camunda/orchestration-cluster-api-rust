"""Hook 07 — full-surface ergonomic facade.

The sibling SDKs (JS, Python, C#) expose a *flat* client with one ergonomic method per
REST operation, each routed through the shared auth / retry / backpressure machinery. In
JS and C# that facade is itself *generated* from the operation list — we do the same here.

This hook parses the generated low-level operation functions in
``client/src/apis/*_api.rs`` (each uniformly shaped as
``pub async fn op(configuration: &Configuration, params: OpParams) -> Result<T, Error<E>>``)
and emits ``src/runtime/facade_generated.rs``: an ``impl CamundaClient`` block with one
method per operation that

  * builds an authenticated ``Configuration`` via ``self.configuration()``,
  * runs the call under ``self.guarded(..)`` (adaptive backpressure + transient retry), and
  * maps the typed ``Error<E>`` to :class:`CamundaError` via the blanket ``From`` impl.

Operations already covered by a hand-written, more-ergonomic curated method (semantic
keys, typed variables, default-tenant injection, deploy-from-file) are skipped so the
nicer versions win — see ``CURATED_SKIP``.
"""

from __future__ import annotations

import re
from pathlib import Path

from .common import Context, snake_case

NUMBER = 7
NAME = "sdk-facade"

# Generated operation function names that collide with — or are superseded by — a
# hand-written curated method on `CamundaClient`. These are intentionally NOT generated.
CURATED_SKIP = {
    # name collisions with curated ergonomic wrappers (would be duplicate methods)
    "activate_jobs",
    "broadcast_signal",
    "cancel_process_instance",
    "complete_job",
    "correlate_message",
    "create_process_instance",
    "evaluate_decision",
    "fail_job",
    "get_process_instance",
    "publish_message",
    "search_variables",
    "throw_job_error",
    # superseded by the curated `topology()` wrapper
    "get_topology",
}

# Matches each generated operation. `params` group is optional (6 ops take no params).
# Tolerant of both rustfmt-formatted output (trailing commas, newlines) and the raw
# openapi-generator output (single line, no trailing commas) — postprocess runs BEFORE
# `cargo fmt`, so we must parse the unformatted form.
_OP_RE = re.compile(
    r"pub async fn (\w+)\(\s*"
    r"configuration: &configuration::Configuration"
    r"(?:\s*,\s*params:\s*(\w+))?"
    r"\s*,?\s*\)\s*->\s*Result<(.*?),\s*Error<",
    re.DOTALL,
)

HEADER = """\
// @generated by scripts/postprocess.py (hook 07) — DO NOT EDIT.
//
// Full-surface ergonomic facade: one method per REST operation, mirroring the flat
// clients of the JS / Python / C# SDKs. Every method builds an authenticated
// `Configuration`, runs the call under the adaptive backpressure gate + transient retry
// policy (`guarded`), and maps the typed low-level error to `CamundaError`.
//
// Operations with a hand-written, more-ergonomic curated wrapper (semantic keys, typed
// variables, default-tenant injection) are implemented in `client.rs` instead.

#![allow(clippy::too_many_lines)]
#![allow(deprecated)]

use crate::{CamundaClient, Result};
use camunda_orchestration_api_client::models;
"""


def _build_doc_map(ctx: Context) -> dict[str, dict]:
    """Map snake_case(operationId) -> {method, path, summary} from the spec, so each
    generated method gets a meaningful doc comment."""
    out: dict[str, dict] = {}
    for path, item in (ctx.spec.get("paths") or {}).items():
        if not isinstance(item, dict):
            continue
        for method, op in item.items():
            if method.lower() not in {
                "get", "post", "put", "patch", "delete", "head", "options",
            }:
                continue
            if not isinstance(op, dict):
                continue
            op_id = op.get("operationId")
            if not op_id:
                continue
            out[snake_case(op_id)] = {
                "method": method.upper(),
                "path": path,
                "summary": (op.get("summary") or op.get("description") or "").strip(),
            }
    return out


def _parse_operations(apis_dir: Path) -> list[dict]:
    ops: list[dict] = []
    for f in sorted(apis_dir.glob("*_api.rs")):
        module = f.stem
        text = f.read_text(encoding="utf-8")
        for m in _OP_RE.finditer(text):
            ops.append(
                {
                    "module": module,
                    "fn": m.group(1),
                    "params": m.group(2),  # ParamsType name or None
                    "ret": m.group(3).strip(),
                }
            )
    return ops


def _first_sentence(text: str) -> str:
    text = " ".join(text.split())
    # Keep doc comments single-line and tidy.
    for sep in (". ", "\n"):
        if sep in text:
            text = text.split(sep, 1)[0]
            break
    return text.rstrip(".")


def _render_method(op: dict, doc: dict | None) -> str:
    module, fn, params, ret = op["module"], op["fn"], op["params"], op["ret"]
    call_path = f"camunda_orchestration_api_client::apis::{module}::{fn}"

    lines: list[str] = []
    if doc and (doc.get("summary") or doc.get("path")):
        summary = _first_sentence(doc.get("summary") or "")
        ref = f"`{doc['method']} {doc['path']}`" if doc.get("path") else ""
        if summary and ref:
            lines.append(f"    /// {summary} ({ref}).")
        elif summary:
            lines.append(f"    /// {summary}.")
        elif ref:
            lines.append(f"    /// {ref}.")
    else:
        lines.append(f"    /// Generated facade for `{module}::{fn}`.")

    if params:
        params_ty = f"camunda_orchestration_api_client::apis::{module}::{params}"
        lines.append(f"    pub async fn {fn}(&self, params: {params_ty}) -> Result<{ret}> {{")
        lines.append("        self.guarded(|| {")
        lines.append("            let params = params.clone();")
        lines.append("            async move {")
        lines.append("                let cfg = self.configuration().await?;")
        lines.append(f"                Ok({call_path}(&cfg, params).await?)")
        lines.append("            }")
        lines.append("        })")
        lines.append("        .await")
        lines.append("    }")
    else:
        lines.append(f"    pub async fn {fn}(&self) -> Result<{ret}> {{")
        lines.append("        self.guarded(|| async {")
        lines.append("            let cfg = self.configuration().await?;")
        lines.append(f"            Ok({call_path}(&cfg).await?)")
        lines.append("        })")
        lines.append("        .await")
        lines.append("    }")
    return "\n".join(lines)


def run(ctx: Context) -> None:
    apis_dir = ctx.client_dir / "src" / "apis"
    if not apis_dir.is_dir():
        ctx.log(NAME, f"WARNING: {apis_dir} not found; skipping facade generation")
        return

    sdk_root = ctx.client_dir.parent
    out_path = sdk_root / "src" / "runtime" / "facade_generated.rs"

    doc_map = _build_doc_map(ctx)
    ops = _parse_operations(apis_dir)

    generated, skipped = [], 0
    for op in sorted(ops, key=lambda o: (o["module"], o["fn"])):
        if op["fn"] in CURATED_SKIP:
            skipped += 1
            continue
        generated.append(_render_method(op, doc_map.get(op["fn"])))

    body = HEADER + "\nimpl CamundaClient {\n" + "\n\n".join(generated) + "\n}\n"
    out_path.write_text(body, encoding="utf-8")

    ctx.log(
        NAME,
        f"wrote {out_path.relative_to(sdk_root)}: "
        f"{len(generated)} generated method(s), {skipped} curated skip(s)",
    )
